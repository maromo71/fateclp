/*
    Função somar: recebe dois valores inteiros e devolve
    a soma dos dois valores.
*/
int somar(int x, int y);

/*
    Função subtrair: recebe dois valores inteiros e devolve
    a diferença do primeiro pelo segundo
*/
int subtrair(int x, int y);

/*
    Função multiplicar: recebe dois valores inteiros e devolve
    o produto do primeiro pelo segundo
*/
int multiplicar(int x, int y);

/*
    Função dividir: recebe dois valores inteiros e devolve
    o resultado da divisão inteira do primeiro pelo segundo.
    OBS: não divide por zero. Apresentará um erro para o usuário
*/
int dividir(int x, int y);

